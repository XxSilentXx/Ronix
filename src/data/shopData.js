// Helper function to find item by ID
export const findItemById = (itemId) => {
  for (const category of shopData.shop.categories) {
    const item = category.items.find(item => item.id === itemId);
    if (item) return item;
  }
  return null;
};

// Helper function to get all items flattened
export const getAllItems = () => {
  return shopData.shop.categories.flatMap(category => category.items);
};

